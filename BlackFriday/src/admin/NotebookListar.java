        NotebookDAO dao = new NotebookDAO();
        List<Notebook> lista = dao.listar();
            
        DefaultTableModel modelo = (DefaultTableModel)tabelaNotebook.getModel();
        
        Object[] linha = new Object [modelo.getColumnCount()]; //tamanho do array

        for (Notebook note : lista) 
        {
            linha[0] = note.getId(); 
            linha[1] = note.getNome();
            linha[2] = note.getMarca();
            linha[3] = note.getDescricao();
            linha[4] = note.getPrecoNormal();
            linha[5] = note.getPrecoBF();
            linha[6] = note.getDestaque();
            modelo.addRow(linha);
    
    }
   
    
